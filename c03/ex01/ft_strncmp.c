int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	h;

	h = 0;
	while (h < n && (s1[h] != '\0' || s2[h] != '\0'))
	{
		if (s1[h] != s2[h])
			return (s1[h] - s2[h]);
		h++;
	}
	return (0);
}
