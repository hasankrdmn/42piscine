int	ft_sqrt(int nb)
{
	int	sq;

	if (nb < 0)
		return (0);
	else if (nb == 1)
		return (1);
	sq = 1;
	while (sq <= nb / 2 && sq <= 46340)
	{
		if (sq * sq == nb)
			return (sq);
		sq++;
	}
	return (0);
}
